import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { execFileSync } from "node:child_process";

const rootDir = path.join(fileURLToPath(new URL(".", import.meta.url)), "..");
const outFile = path.join(rootDir, "src/data/latestUpdate.js");

// Commits opt in to being surfaced on the Home dashboard by adding a
// "User-Update:" line (+ optional "User-Update-Item:" lines) to the commit
// body -- everything else about the commit message stays dev-facing as
// normal. Looks back over recent commits and uses the newest one that has
// the marker; if none do, leaves the existing generated file untouched
// rather than clobbering it with nothing.
const LOOKBACK = 50;
const DELIMITER = "===COMMIT-END===";

function findMarkedCommit() {
  let log;
  try {
    // execFileSync passes args directly to the git process with no shell
    // involved, avoiding shell quoting/escaping entirely.
    log = execFileSync("git", ["log", "--format=%B" + DELIMITER, "-n", String(LOOKBACK)], { cwd: rootDir, encoding: "utf8" });
  } catch {
    return null;
  }

  const commits = log.split(DELIMITER).map(s => s.trim()).filter(Boolean);

  for (const body of commits) {
    const headingMatch = body.match(/^User-Update:\s*(.+)$/m);
    if (!headingMatch) continue;

    const items = [...body.matchAll(/^User-Update-Item:\s*(.+)$/gm)].map(m => m[1].trim());
    return { heading: headingMatch[1].trim(), items };
  }

  return null;
}

const found = findMarkedCommit();

if (found) {
  const contents = "// Auto-generated by scripts/generate-latest-update.js from the newest commit\n"
    + "// with a \"User-Update:\" marker -- do not hand-edit, it will be overwritten on\n"
    + "// the next build. To change what this shows, make a new commit whose body\n"
    + "// includes:\n"
    + "//   User-Update: <heading>\n"
    + "//   User-Update-Item: <bullet>\n"
    + "//   User-Update-Item: <bullet>\n"
    + "export const LATEST_UPDATE = " + JSON.stringify(found, null, 2) + ";\n";

  writeFileSync(outFile, contents);
  console.log(`generate-latest-update: wrote "${found.heading}" to src/data/latestUpdate.js`);
} else {
  console.log("generate-latest-update: no commit with a User-Update marker found, leaving src/data/latestUpdate.js as-is.");
}
